// ============================================================
//  Testbench - Vedic Multiplier Full Hierarchy
//  Tests: 2x2, 4x4, 8x8, 16x16, 32x32, 64x64
// ============================================================

`timescale 1ns/1ps

module vedic_multiplier_tb;

    // --------------------------------------------------------
    //  2x2 Signals
    // --------------------------------------------------------
    reg  [1:0]  a2, b2;
    wire [3:0]  s2;

    // --------------------------------------------------------
    //  4x4 Signals
    // --------------------------------------------------------
    reg  [3:0]  a4, b4;
    wire [7:0]  s4;

    // --------------------------------------------------------
    //  8x8 Signals
    // --------------------------------------------------------
    reg  [7:0]  a8, b8;
    wire [15:0] s8;

    // --------------------------------------------------------
    //  16x16 Signals
    // --------------------------------------------------------
    reg  [15:0] a16, b16;
    wire [31:0] s16;

    // --------------------------------------------------------
    //  32x32 Signals
    // --------------------------------------------------------
    reg  [31:0] a32, b32;
    wire [63:0] s32;

    // --------------------------------------------------------
    //  64x64 Signals
    // --------------------------------------------------------
    reg  [63:0] a64, b64;
    wire [127:0] s64;

    // --------------------------------------------------------
    //  DUT Instantiations
    // --------------------------------------------------------
    vedicmul_2  dut2  (.a(a2),  .b(b2),  .s(s2));
    vedicmul_4  dut4  (.a(a4),  .b(b4),  .s(s4));
    vedicmul_8  dut8  (.a(a8),  .b(b8),  .s(s8));
    vedicmul_16 dut16 (.a(a16), .b(b16), .s(s16));
    vedicmul_32 dut32 (.a(a32), .b(b32), .s(s32));
    vedicmul_64 dut64 (.a(a64), .b(b64), .s(s64));

    // --------------------------------------------------------
    //  Pass/Fail Counter
    // --------------------------------------------------------
    integer pass, fail;

    // --------------------------------------------------------
    //  Task: Check and Report
    // --------------------------------------------------------
    task check;
        input [127:0] got;
        input [127:0] expected;
        input [127:0] op_a;
        input [127:0] op_b;
        input [7:0]   width;
        begin
            if (got === expected) begin
                $display("  PASS | %0dx%0d | %0d x %0d = %0d", width, width, op_a, op_b, got);
                pass = pass + 1;
            end else begin
                $display("  FAIL | %0dx%0d | %0d x %0d | got=%0d | expected=%0d", width, width, op_a, op_b, got, expected);
                fail = fail + 1;
            end
        end
    endtask

    // --------------------------------------------------------
    //  Main Test
    // --------------------------------------------------------
    initial begin
        pass = 0;
        fail = 0;

        $display("============================================");
        $display("   VEDIC MULTIPLIER TESTBENCH");
        $display("============================================");

        // ----------------------------------------------------
        //  2x2 Tests
        // ----------------------------------------------------
        $display("\n--- 2x2 Multiplier ---");

        a2=2'd0;  b2=2'd0;  #10; check(s2, a2*b2, a2, b2, 8'd2);
        a2=2'd1;  b2=2'd1;  #10; check(s2, a2*b2, a2, b2, 8'd2);
        a2=2'd2;  b2=2'd3;  #10; check(s2, a2*b2, a2, b2, 8'd2);
        a2=2'd3;  b2=2'd3;  #10; check(s2, a2*b2, a2, b2, 8'd2);
        a2=2'd3;  b2=2'd1;  #10; check(s2, a2*b2, a2, b2, 8'd2);
        a2=2'd2;  b2=2'd2;  #10; check(s2, a2*b2, a2, b2, 8'd2);

        // ----------------------------------------------------
        //  4x4 Tests
        // ----------------------------------------------------
        $display("\n--- 4x4 Multiplier ---");

        a4=4'd0;   b4=4'd0;   #10; check(s4, a4*b4, a4, b4, 8'd4);
        a4=4'd1;   b4=4'd1;   #10; check(s4, a4*b4, a4, b4, 8'd4);
        a4=4'd3;   b4=4'd5;   #10; check(s4, a4*b4, a4, b4, 8'd4);
        a4=4'd7;   b4=4'd7;   #10; check(s4, a4*b4, a4, b4, 8'd4);
        a4=4'd15;  b4=4'd15;  #10; check(s4, a4*b4, a4, b4, 8'd4);
        a4=4'd12;  b4=4'd9;   #10; check(s4, a4*b4, a4, b4, 8'd4);
        a4=4'd6;   b4=4'd4;   #10; check(s4, a4*b4, a4, b4, 8'd4);
        a4=4'd15;  b4=4'd1;   #10; check(s4, a4*b4, a4, b4, 8'd4);

        // ----------------------------------------------------
        //  8x8 Tests
        // ----------------------------------------------------
        $display("\n--- 8x8 Multiplier ---");

        a8=8'd0;    b8=8'd0;    #10; check(s8, a8*b8, a8, b8, 8'd8);
        a8=8'd1;    b8=8'd1;    #10; check(s8, a8*b8, a8, b8, 8'd8);
        a8=8'd15;   b8=8'd15;   #10; check(s8, a8*b8, a8, b8, 8'd8);
        a8=8'd255;  b8=8'd255;  #10; check(s8, a8*b8, a8, b8, 8'd8);
        a8=8'd128;  b8=8'd2;    #10; check(s8, a8*b8, a8, b8, 8'd8);
        a8=8'd200;  b8=8'd100;  #10; check(s8, a8*b8, a8, b8, 8'd8);
        a8=8'd123;  b8=8'd45;   #10; check(s8, a8*b8, a8, b8, 8'd8);
        a8=8'd255;  b8=8'd1;    #10; check(s8, a8*b8, a8, b8, 8'd8);

        // ----------------------------------------------------
        //  16x16 Tests
        // ----------------------------------------------------
        $display("\n--- 16x16 Multiplier ---");

        a16=16'd0;      b16=16'd0;      #10; check(s16, a16*b16, a16, b16, 8'd16);
        a16=16'd1;      b16=16'd1;      #10; check(s16, a16*b16, a16, b16, 8'd16);
        a16=16'd255;    b16=16'd255;    #10; check(s16, a16*b16, a16, b16, 8'd16);
        a16=16'd65535;  b16=16'd65535;  #10; check(s16, a16*b16, a16, b16, 8'd16);
        a16=16'd1000;   b16=16'd1000;   #10; check(s16, a16*b16, a16, b16, 8'd16);
        a16=16'd32768;  b16=16'd2;      #10; check(s16, a16*b16, a16, b16, 8'd16);
        a16=16'd12345;  b16=16'd678;    #10; check(s16, a16*b16, a16, b16, 8'd16);
        a16=16'd65535;  b16=16'd1;      #10; check(s16, a16*b16, a16, b16, 8'd16);

        // ----------------------------------------------------
        //  32x32 Tests
        // ----------------------------------------------------
        $display("\n--- 32x32 Multiplier ---");

        a32=32'd0;          b32=32'd0;          #10; check(s32, a32*b32, a32, b32, 8'd32);
        a32=32'd1;          b32=32'd1;          #10; check(s32, a32*b32, a32, b32, 8'd32);
        a32=32'd65535;      b32=32'd65535;      #10; check(s32, a32*b32, a32, b32, 8'd32);
        a32=32'hFFFFFFFF;   b32=32'hFFFFFFFF;   #10; check(s32, a32*b32, a32, b32, 8'd32);
        a32=32'd100000;     b32=32'd100000;     #10; check(s32, a32*b32, a32, b32, 8'd32);
        a32=32'h80000000;   b32=32'd2;          #10; check(s32, a32*b32, a32, b32, 8'd32);
        a32=32'd123456789;  b32=32'd987;        #10; check(s32, a32*b32, a32, b32, 8'd32);
        a32=32'hFFFFFFFF;   b32=32'd1;          #10; check(s32, a32*b32, a32, b32, 8'd32);

        // ----------------------------------------------------
        //  64x64 Tests
        // ----------------------------------------------------
        $display("\n--- 64x64 Multiplier ---");

        a64=64'd0;              b64=64'd0;              #10; check(s64, a64*b64, a64, b64, 8'd64);
        a64=64'd1;              b64=64'd1;              #10; check(s64, a64*b64, a64, b64, 8'd64);
        a64=64'hFFFFFFFF;       b64=64'hFFFFFFFF;       #10; check(s64, a64*b64, a64, b64, 8'd64);
        a64=64'hFFFFFFFFFFFFFFFF; b64=64'hFFFFFFFFFFFFFFFF; #10; check(s64, a64*b64, a64, b64, 8'd64);
        a64=64'd1000000000;     b64=64'd1000000000;     #10; check(s64, a64*b64, a64, b64, 8'd64);
        a64=64'h8000000000000000; b64=64'd2;            #10; check(s64, a64*b64, a64, b64, 8'd64);
        a64=64'd123456789012345; b64=64'd9876;          #10; check(s64, a64*b64, a64, b64, 8'd64);
        a64=64'hFFFFFFFFFFFFFFFF; b64=64'd1;            #10; check(s64, a64*b64, a64, b64, 8'd64);

        // ----------------------------------------------------
        //  Summary
        // ----------------------------------------------------
        $display("\n============================================");
        $display("   RESULTS: %0d PASSED | %0d FAILED", pass, fail);
        $display("============================================\n");

        $finish;
    end

endmodule
